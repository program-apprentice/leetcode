public class MainUtil {
    public static void printListString(List<String> list) {
        for(String s : list) {
            System.out.println(s);
        }
    }

    public static void printListToken(List<String> list) {
        for(String s : list) {
            System.out.print(s + " ");
        }
        System.out.println();
    }
}
